// Function to get the contracts by owner and display them as collection blocks
async function getContractsByOwner(owner) {
  try {

    // Get the contracts by owner
    // const owner = ethersProvider.selectedAddress;
    const contracts = await factoryContract.getContractsByOwner(owner);

    // Clear the existing collection blocks
    const collectionContainer = document.querySelector(".collection-container");
    collectionContainer.innerHTML = "";

    // Loop through the contracts and display them as collection blocks
    contracts.forEach(async (contractAddress) => {
      // const contract = connectNFTContract(contractAddress);
      const contract = new ethers.Contract(contractAddress, NFTContractAbi, ethersProvider.getSigner());
      const contractName = await contract.getname();
      const collectionBlock = document.createElement("div");
      collectionBlock.classList.add("collection-block");
	  collectionBlock.onclick = function() {window.location.href = "collection.html?contractAddress=" + contractAddress + "&contractName=" + contractName};
      const collectionName = document.createElement("h2");
      collectionName.innerText = `Contract Name: ${contractName}`;
      collectionBlock.appendChild(collectionName);
      const collectionAddress = document.createElement("p");
      collectionAddress.innerText = `Contract Address: ${contractAddress}`;
      collectionBlock.appendChild(collectionAddress);
      collectionContainer.appendChild(collectionBlock);
    });
  } catch (error) {
    console.error(error);
  }
}


// Function to add a new collection
async function createCollection() {
  // Prompt the user to enter the name and symbol for the collection
  const name = prompt("Enter the name for the collection:");
  const symbol = prompt("Enter the symbol for the collection:");

  // Call the createContract function from the factory contract
  try {


    // Call the createContract function
    const tx = await factoryContract.createContract(name, symbol);

    // Wait for the transaction to be mined
    await tx.wait();

    // Refresh the collection blocks
    getContractsByOwner();
  } catch (error) {
    console.error(error);
  }
}


