async function connectWallet() {
  try {
    // Prompt the user to connect their wallet
    ethersProvider = new ethers.providers.Web3Provider(window.ethereum);
    await ethereum.enable();
    connectFactory();
    
    // Get the connected wallet's address
    const accounts = await ethersProvider.listAccounts();
    if (accounts.length > 0) {
      connectedWalletAddress = accounts[0];
      updateWalletAddress();
    }
  } catch (error) {
    console.error(error);
  }
}

// Function to update the wallet address display
function updateWalletAddress() {
  const walletAddressElement = document.getElementById("connect-wallet-button");
  if (connectedWalletAddress) {
    walletAddressElement.innerText = `Connected Wallet: ${connectedWalletAddress}`;
    document.getElementById("connect-wallet-button").style.backgroundColor = "green";
  } else {
    walletAddressElement.innerText = "";
    document.getElementById("connect-wallet-button").style.backgroundColor = "";
  }

  getContractsByOwner(connectedWalletAddress);
}

